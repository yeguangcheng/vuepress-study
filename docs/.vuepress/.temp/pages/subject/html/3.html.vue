<h4 id="title和h1的区别" tabindex="-1"><a class="header-anchor" href="#title和h1的区别" aria-hidden="true">#</a> title和h1的区别</h4>
<ul>
<li><strong>定义</strong>：title是网站标题，h1是文章主题。</li>
<li><strong>作用</strong>：title概括网站信息，可以直接告诉搜索引擎和用户这 个网站是关于什么主题和内容的，是显示在网页Tab栏里的；<br>
h1突出文章主题，面对用户，更突出其视觉效果，指向 页面主体信息，是显示在网页中的。</li>
</ul>
<h4 id="b与strong的区别" tabindex="-1"><a class="header-anchor" href="#b与strong的区别" aria-hidden="true">#</a> b与strong的区别</h4>
<ul>
<li><strong>定义</strong>：b(bold)是实体标签，用来给文字加粗，strong是逻辑标签，作用是加强字符语气。</li>
<li><strong>区别</strong>：b标签只是加粗的样式，没有实际含义，常用来表达无强调或着重意味的粗体文字，比如文章摘要中的关键词、 评测文章中的产品名称、文章的导言；<br>
strong表示标签内字符重要，用以强调，其默认格式是加粗，但是可以通 过CSS添加样式，使用别的样式强调。</li>
</ul>
<h4 id="i与em的区别" tabindex="-1"><a class="header-anchor" href="#i与em的区别" aria-hidden="true">#</a> i与em的区别</h4>
<ul>
<li><strong>定义</strong>：i(italic)是实体标签，用来使字符倾斜，em(emphasis)是逻辑标签，作用是强调文本内容。</li>
<li><strong>区别</strong>：i标签只是斜体的样式，没有实际含义，常用来表达无强调或着重意味的斜体，比如生物学名、术语、外来语；<br>
em表示标签内字符重要，用以强调，其默认格式是斜体，但是可以通过CSS添加样式。</li>
</ul>
</template>
